// Assignments: Toán tử gán
let x = 10 // Gán giá trị 10 cho biến x
console.log(x); // 10
// Để ý: Toán tử gán có thể kết hợp với toán tử số học để viết ngắn gọn hơn.
x += 5; // Tương đương với x = x + 5
console.log(x); // 15
x -= 5; // Tương đương với x = x - 5
console.log(x); // 10
x *= 5; // Tương đương với x = x * 5
console.log(x); // 50
x /= 5; // Tương đương với x = x / 5
console.log(x); // 10
x %= 5; // Tương đương với x = x % 5
console.log(x); // 0
x **= 5; // Tương đương với x = x ** 5
console.log(x); // 0

