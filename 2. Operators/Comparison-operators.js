// Comparisons: Toán tử so sánh
let x = 10;
let y = 5;

console.log(x > y); // Lớn hơn: true
console.log(x < y); // Nhỏ hơn: false

console.log(x >= y); // Lớn hơn hoặc bằng: true
console.log(x <= y); // Nhỏ hơn hoặc bằng: false

console.log(x == y); // Bằng: false
console.log(x === y); // Bằng về giá trị và kiểu dữ liệu: false

console.log(x != y); // Không bằng: true
console.log(x !== y); // Không bằng về giá trị và kiểu dữ liệu: true
