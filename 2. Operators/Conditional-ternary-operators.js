// Conditional (Ternary) Operators: Toán tử ba ngôi.
// Toán tử ba ngôi được sử dụng để viết ngắn gọn hơn cho các điều kiện if-else.

let age = 21;
let canDoSomething = (age >= 18) ? 'Can' : 'Cannot'; // Nếu đáp ứng đieeuf kiện thì trả về 'Can', ngược lại trả về 'Cannot'
console.log(canDoSomething); // Can