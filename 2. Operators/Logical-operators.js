// Logical operators: Toán tử logic
// Toán tử logic được sử dụng để kết hợp các điều kiện.

let x = 10;
let y = 5;

console.log(x > 5 && y < 10); // AND: true
console.log(x > 5 && y > 10); // AND: false

console.log(x > 5 || y < 10); // OR: true
console.log(x < 5 || y > 10); // OR: false

console.log(!(x > 5)); // NOT: false
console.log(!(x < 5)); // NOT: true

