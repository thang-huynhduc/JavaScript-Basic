// String Operators: Toán tử chuỗi.
// Toán tử chuỗi được sử dụng để kết hợp các chuỗi.

let firstName = "Thang";
let lastName = "Huynh Duc";

console.log(firstName + " -- " + lastName); // Kết hợp chuỗi: Thang -- Huynh Duc
// String Literals: Chuỗi mẫu.
// Chuỗi mẫu là chuỗi được bao quanh bởi dấu nháy đơn hoặc dấu nháy kép.
console.log(`Hello, I am ${firstName} ${lastName}.`); // Hello, I am Thang Huynh Duc.