// Trong JS, vòng lặp (loop) là một cấu trúc điều khiển dùng đề lặp lại một đoạn mã nhiều lần.
// Thường được sử dụng khi cần thực hiện một hành động nào đó nhiều lần cho đến khi một điều kiện nào đó được thõa mãn.

// for..of: Dùng khi biết trước số lần lặp.
for (let i = 0; i <= 5; i++) {
    console.log('Count: ', i);
}
console.log('---------------------');

for ( let i = 10; i>=0; i--) {
    console.log('Count: ', i);
}